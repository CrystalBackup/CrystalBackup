# shellcheck shell=bash
# Source me — never execute: `source scripts/load-env.sh`
#
# Exports the credentials the crucible needs from the repo's .secrets/ dir
# (git-ignored; see secrets.example/ for the expected layout):
#
#   HETZNER_TOKEN        raw Hetzner Cloud API token   -> HCLOUD_TOKEN
#   HETZNER_S3           ACCESS_TOKEN= / SECRET_KEY=   -> AWS_* + TF_VAR_s3_*
#   crystalbackup.key    SSH private key               -> ANSIBLE_PRIVATE_KEY_FILE
#
# Also sources artifacts/crucible.env (non-secret facts written by terraform:
# S3_BUCKET, S3_ENDPOINT, MASTER1_PUBLIC_IP).
#
# Values are exported into the environment only — never written to disk,
# never echoed.

_crucible_dir="$(cd "$(dirname "${BASH_SOURCE[0]}")/.." && pwd)"
_repo_root="$(cd "${_crucible_dir}/../.." && pwd)"
_secrets_dir="${CRYSTALBACKUP_SECRETS_DIR:-${_repo_root}/.secrets}"

if [[ ! -d "${_secrets_dir}" ]]; then
  echo "load-env: WARNING — no secrets dir at ${_secrets_dir} (see test/crucible/secrets.example/)" >&2
fi

if [[ -f "${_secrets_dir}/HETZNER_TOKEN" ]]; then
  HCLOUD_TOKEN="$(tr -d '[:space:]' < "${_secrets_dir}/HETZNER_TOKEN")"
  export HCLOUD_TOKEN
fi

if [[ -f "${_secrets_dir}/HETZNER_S3" ]]; then
  ACCESS_TOKEN="" SECRET_KEY=""
  # shellcheck disable=SC1091
  source "${_secrets_dir}/HETZNER_S3"
  export AWS_ACCESS_KEY_ID="${ACCESS_TOKEN}"
  export AWS_SECRET_ACCESS_KEY="${SECRET_KEY}"
  export TF_VAR_s3_access_key="${ACCESS_TOKEN}"
  export TF_VAR_s3_secret_key="${SECRET_KEY}"
  unset ACCESS_TOKEN SECRET_KEY
fi

if [[ -f "${_secrets_dir}/crystalbackup.key" ]]; then
  export ANSIBLE_PRIVATE_KEY_FILE="${_secrets_dir}/crystalbackup.key"
fi

# Non-secret facts generated by `make infra` (safe to source, git-ignored).
if [[ -f "${_crucible_dir}/artifacts/crucible.env" ]]; then
  set -a
  # shellcheck disable=SC1091
  source "${_crucible_dir}/artifacts/crucible.env"
  set +a
fi

unset _crucible_dir _repo_root _secrets_dir
